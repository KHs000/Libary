    private void login_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_buttonActionPerformed
        // TODO add your handling code here:
        if (evt.getSource() == login_button) {
                try{
                    MysqlDataSource dataSource = new MysqlDataSource();
                    dataSource.setUser("root");
                    dataSource.setPassword("");
                    dataSource.setDatabaseName("biblioteca");
                    dataSource.setServerName("localhost");
                    Connection conn = dataSource.getConnection();
                    PreparedStatement stmt = conn.prepareStatement("SELECT Login,Senha FROM admin WHERE Login LIKE ? AND Senha LIKE ?");
                    stmt.setString(1,fieldlogin.getText());
                    stmt.setString(2,fieldsenha.getText());
                    
                    ResultSet result = stmt.executeQuery();
                    if (!result.next()) {
                        JOptionPane.showMessageDialog(null, "Credenciais inv√°lidas.");
                    }
                    else {
                        // Acesso permitido
                        JOptionPane.showMessageDialog(null, "Logado");
                        dispose();
                        Home_Screen_Adm adm = new Home_Screen_Adm(fieldlogin.getText()); 
                        
                        adm.setVisible(true);
                        adm.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                        adm.setSize(935,665);
                    }
                } catch(SQLException ex){
                    ex.printStackTrace();
                }     
            }

    }//GEN-LAST:event_login_buttonActionPerformed

